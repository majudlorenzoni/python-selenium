int1 = int(input('Digite um número inteiro: '))
int2 = int(input('Digite outro número inteiro: '))
float3 = float(input('Digite um número real: '))

result1 = (2 * int1) * (int2 / 2)
result2 = (3 * int1) + float3
result3 = float3 ** 3

print(f'O resultado da expressão 1 é: {result1}')
print(f'O resultado da expressão 2 é: {result2}')
print(f'O resultado da expressão 3 é: {result3}')